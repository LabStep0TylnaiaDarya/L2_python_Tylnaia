import random  # Импортируем модуль для генерации случайных чисел

# Генерируем случайное число от 0 до 10 и сохраняем его в переменную secret
secret = random.randint(0, 10)

# Выводим сообщение с инструкцией для пользователя
print("Я загадал число от 0 до 10. У тебя есть 3 попытки.")

# Начинаем цикл из 3 попыток (от 1 до 3 включительно)
for attempt in range(1, 4):
    try:
        # Запрашиваем ввод пользователя и сразу пытаемся преобразовать его в целое число
        guess = int(input(f"Попытка {attempt}: "))
    except ValueError:
        # Если пользователь ввёл не число, сообщаем об этом и переходим к следующей попытке
        print("Пожалуйста, введите целое число.")
        continue  # Переходим к следующей итерации цикла

    # Если введённое число равно загаданному
    if guess == secret:
        print("Поздравляю! Ты угадал число!")
        break  # Завершаем цикл — число угадано
    # Если введённое число меньше загаданного
    elif guess < secret:
        print("Не верно! Загаданное число больше!")
    # В остальных случаях (если больше)
    else:
        print("Не верно! Загаданное число меньше!")
# Этот блок else выполнится только если цикл завершится без break (т.е. не угадали)
else:
    print(f"К сожалению, ты не угадал. Загаданное число было {secret}.")
